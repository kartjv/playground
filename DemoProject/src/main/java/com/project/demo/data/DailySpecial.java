package com.project.demo.data;

public class DailySpecial {

	private String specialName;
	private String briefRecipe;
	/**
	 * @return the specialName
	 */
	public String getSpecialName() {
		return specialName;
	}
	/**
	 * @param specialName the specialName to set
	 */
	public void setSpecialName(String specialName) {
		this.specialName = specialName;
	}
	/**
	 * @return the briefRecipe
	 */
	public String getBriefRecipe() {
		return briefRecipe;
	}
	/**
	 * @param briefRecipe the briefRecipe to set
	 */
	public void setBriefRecipe(String briefRecipe) {
		this.briefRecipe = briefRecipe;
	}

	
}
